/***

Subtask-1:

Find all the odd numbers from 61 to 100.

 */

let oddNum = 60;
while (oddNum <= 100) {
    if (oddNum % 2 != 0) {
        console.log('Odd Numbers:', oddNum);
    }
    oddNum++;

}

/***

Subtask-2:

Find all the even numbers from 78 to 98.

 */

let evenNum = 78;
while (evenNum <= 98) {
    if (evenNum % 2 === 0) {
        console.log('Even Numbers:', evenNum);
    }
    evenNum++;

}


